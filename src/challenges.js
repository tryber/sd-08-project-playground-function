function splitSentence(frase) {
  let palavras = [];
  let palavra = '';
  for (let index = 0; index <= frase.length; index += 1) {
    if (frase[index] === ' '){
      palavras.push(palavra);
      palavra = '';
    }
    else if (index === frase.length-1) {
      palavra = palavra+frase[index];
      palavras.push(palavra);
    }
    else {
      palavra = palavra+frase[index];
    }
  }
  return palavras;
}

